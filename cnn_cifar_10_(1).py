class CNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.conv1 = nn.Conv2d(3, 32, 3, padding=1)
        self.conv2 = nn.Conv2d(32, 64, 3, padding=1)
        self.pool  = nn.MaxPool2d(2,2)       # 32->16, 64->8
        self.fc1   = nn.Linear(64*8*8, 120)  # cuidado: se mudar pooling, atualize aqui
        self.fc2   = nn.Linear(120, 84)
        self.fc3   = nn.Linear(84, 10)

    def forward(self, x):
        x = self.pool(F.relu(self.conv1(x))) # 32x16x16
        x = self.pool(F.relu(self.conv2(x))) # 64x8x8
        x = x.view(x.size(0), -1)
        x = F.relu(self.fc1(x))
        x = F.relu(self.fc2(x))
        return self.fc3(x)  # logits

def make_optimizer(name, params, lr, momentum=None):
    if name == "Adam":
        return torch.optim.Adam(params, lr=lr)
    if name == "RMSprop":
        return torch.optim.RMSprop(params, lr=lr, momentum=0.9)
    if name == "SGD":
        return torch.optim.SGD(params, lr=lr, momentum=(momentum or 0.9), nesterov=True)
    raise ValueError(name)

def train_one_epoch(model, loader, optimizer, criterion):
    model.train()
    tot_loss, correct, total = 0.0, 0, 0
    for x,y in loader:
        x,y = x.to(device), y.to(device)
        optimizer.zero_grad()
        logits = model(x)
        loss = criterion(logits, y)
        loss.backward(); optimizer.step()
        tot_loss += loss.item() * x.size(0)
        correct  += (logits.argmax(1)==y).sum().item()
        total    += x.size(0)
    return tot_loss/total, correct/total

@torch.no_grad()
def evaluate(model, loader, criterion):
    model.eval()
    tot_loss, correct, total = 0.0, 0, 0
    for x,y in loader:
        x,y = x.to(device), y.to(device)
        logits = model(x)
        loss = criterion(logits, y)
        tot_loss += loss.item() * x.size(0)
        correct  += (logits.argmax(1)==y).sum().item()
        total    += x.size(0)
    return tot_loss/total, correct/total

