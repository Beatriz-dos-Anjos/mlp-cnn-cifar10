from torch.optim.lr_scheduler import LinearLR, SequentialLR, CosineAnnealingLR

def train_one_epoch(model, loader, criterion, optimizer):
    model.train()
    total_loss, y_true, y_pred = 0.0, [], []
    for x, y in loader:
        x, y = x.to(device), y.to(device)
        logits = model(x)
        loss = criterion(logits, y)
        optimizer.zero_grad(set_to_none=True)
        loss.backward()
        optimizer.step()
        total_loss += loss.item() * x.size(0)
        y_true.extend(y.tolist())
        y_pred.extend(logits.argmax(1).tolist())
    avg_loss = total_loss / len(loader.dataset)
    acc = accuracy_score(y_true, y_pred)
    return avg_loss, acc

@torch.no_grad()
def evaluate(model, loader, criterion):
    model.eval()
    total_loss, y_true, y_pred = 0.0, [], []
    for x, y in loader:
        x, y = x.to(device), y.to(device)
        logits = model(x)
        loss = criterion(logits, y)
        total_loss += loss.item() * x.size(0)
        y_true.extend(y.tolist())
        y_pred.extend(logits.argmax(1).tolist())
    avg_loss = total_loss / len(loader.dataset)
    acc = accuracy_score(y_true, y_pred)
    return avg_loss, acc

def fit(model, train_loader, val_loader,
        epochs=50, base_lr=0.1, momentum=0.9, weight_decay=5e-4,
        patience=12, min_epochs=20, label_smoothing=0.1, use_warmup=True):

    criterion = nn.CrossEntropyLoss(label_smoothing=label_smoothing)
    optimizer = torch.optim.SGD(model.parameters(), lr=base_lr, momentum=momentum,
                                weight_decay=weight_decay, nesterov=True)

    if use_warmup and epochs > 10:
        warmup = LinearLR(optimizer, start_factor=0.2, total_iters=5)
        cosine = CosineAnnealingLR(optimizer, T_max=epochs-5)
        scheduler = SequentialLR(optimizer, [warmup, cosine], milestones=[5])
    else:
        scheduler = CosineAnnealingLR(optimizer, T_max=epochs)

    history = {"train_loss": [], "train_acc": [], "val_loss": [], "val_acc": []}
    best_val = float("inf")
    best_state = None
    wait = 0

    for ep in range(1, epochs+1):
        tr_loss, tr_acc = train_one_epoch(model, train_loader, criterion, optimizer)
        vl_loss, vl_acc = evaluate(model, val_loader, criterion)

        history["train_loss"].append(tr_loss); history["train_acc"].append(tr_acc)
        history["val_loss"].append(vl_loss);   history["val_acc"].append(vl_acc)

        print(f"Epoch {ep:03d} | train_loss={tr_loss:.4f} train_acc={tr_acc:.4f} | "
              f"val_loss={vl_loss:.4f} val_acc={vl_acc:.4f} | lr={scheduler.get_last_lr()[0]:.5f}")

        improved = vl_loss < (best_val - 1e-4)
        if improved:
            best_val = vl_loss
            best_state = {k: v.detach().cpu() for k, v in model.state_dict().items()}
            wait = 0
        else:
            if ep >= min_epochs:
                wait += 1
                if wait >= patience:
                    print("Early stopping!")
                    break

        scheduler.step()

    if best_state is not None:
        model.load_state_dict(best_state)

    os.makedirs("checkpoints", exist_ok=True)
    torch.save(model.state_dict(), "checkpoints/cnn3_pessoa3_best.pt")
    print("Checkpoint salvo em checkpoints/cnn3_pessoa3_best.pt")
    return model, history

