class CNN3(nn.Module):
    def __init__(self, p=0.2, use_bn=True, num_classes=10, kernel_size=3):
        super().__init__()
        ks = kernel_size; pad = ks // 2

        def block(cin, cout):
            layers = [nn.Conv2d(cin, cout, ks, padding=pad, bias=False)]
            if use_bn: layers.append(nn.BatchNorm2d(cout))
            layers += [nn.ReLU(inplace=True),
                       nn.Conv2d(cout, cout, ks, padding=pad, bias=False)]
            if use_bn: layers.append(nn.BatchNorm2d(cout))
            layers += [nn.ReLU(inplace=True)]
            return nn.Sequential(*layers)

        self.features = nn.Sequential(
            block(3, 64), nn.MaxPool2d(2), nn.Dropout(p),
            block(64,128), nn.MaxPool2d(2), nn.Dropout(p),
            block(128,256), nn.MaxPool2d(2), nn.Dropout(p),
        )
        self.head = nn.Sequential(
            nn.AdaptiveAvgPool2d(1),
            nn.Flatten(),
            nn.Dropout(p),
            nn.Linear(256, num_classes)
        )

    def forward(self, x):
        x = self.features(x)
        x = self.head(x)
        return x

